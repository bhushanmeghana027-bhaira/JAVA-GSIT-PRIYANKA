package NumberPractise;

public class Swap {
	public static void main(String[] args) {
		int h =5,b=6,temp;
		temp = h;
		h = b;
		b = temp;
		
		System.out.println(h);
		System.out.println(b);
		
		
		int h1=56,h6=98;
		
		h1 = h1+h6;
		h6 = h1-h6;
		h1 = h1-h6;
		
		System.out.println(h1);
		System.out.println(h6);
	}
}
